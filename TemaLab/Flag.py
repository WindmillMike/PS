def construieste_dqt(Q_jpeg):
    data = []
    data.extend(DQT)

    data.extend([0x00, 67]) # 67 pentru ca avem 2 de la 0x00 cu 1 de la lungime + 64 de la matrice in sine
    data.append(0x00)

    q_spirala = spirala(Q_jpeg).astype(np.uint8)
    data.extend(q_spirala.tolist())

    return data


def construieste_sof0(inaltime, latime):
    data = []
    data.extend(SOF0)

    lungime = 11  # Lungime fixă pentru 1 componentă (Grayscale)
    data.extend([0x00, lungime])

    data.append(0x08)  # Precizia (8 biți)

    data.extend([inaltime >> 8, inaltime & 0xFF])
    data.extend([latime >> 8, latime & 0xFF])

    data.append(0x01)  # Număr de componente

    # ID = 1, Factori de esantionare = 1x1, ID Tabela Q  = 0
    data.extend([0x01, 0x11, 0x00])
    return data


def calculeaza_lungime_dht():
    len_dc = 1 + 16 + len(VALORI_DC_Y)
    len_ac = 1 + 16 + len(VALORI_AC_Y)
    return len_dc + len_ac + 2  # Adunăm 2 pentru octeții de Lungime


def construieste_dht():
    data = []
    data.extend(DHT)

    lungime_totala = calculeaza_lungime_dht()
    data.extend([lungime_totala >> 8, lungime_totala & 0xFF])

    data.append(0x00)  # Tip 0 (DC), ID 0 (Y)
    data.extend(COUNT_DC_Y)  # 16 octeți
    data.extend(VALORI_DC_Y)  # 12 octeți

    data.append(0x10)  # Tip 1 (AC), ID 0 (Y)
    data.extend(COUNT_AC_Y)  # 16 octeți
    data.extend(VALORI_AC_Y)  # 162 octeți

    return data

def construieste_sos():
    data = []
    data.extend(SOS)
    data.extend([0x00, 0x08])
    data.append(0x01)
    data.extend([0x01, 0x00]) # ID 1, Tabele DC ID 0 (bits 4-7) si AC ID 0 (bits 0-3)
    data.extend([0x00, 0x3F, 0x00])
    return data
